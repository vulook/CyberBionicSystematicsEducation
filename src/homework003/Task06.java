public class Task06 {

    public static void main(String[] args) {

        int a = 36, b = -11;
        System.out.println("a: " + a + ", b: " + b);

        // addition operator
        System.out.println("a + b = " + (a + b));

        // subtraction operator
        System.out.println("a - b = " + (a - b));

        // multiplication operator
        System.out.println("a * b = " + (a * b));

        // division operator
        System.out.println("a / b = " + (a / b));

        // modulo operator
        System.out.println("a % b = " + (a % b));

    }

}
